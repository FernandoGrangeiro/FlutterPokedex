class Stats {
  Stats(int spd, int atk, int def, int specialAtk, int specialDef, int hp) {
    this.speed = spd;
    this.attack = atk;
    this.defense = def;
    this.specialAttack = specialAtk;
    this.specialDefense = specialDef;
    this.hp = hp;
  }
  int speed;
  int attack;
  int defense;
  int specialAttack;
  int specialDefense;
  int hp;
}
